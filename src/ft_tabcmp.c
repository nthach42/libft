#include "libft.h"

int			ft_tabcmp(char **tab1, char **tab2)
{
	int		i;
	
	i = 0;
	while (tab1[i] != '\0' && tab2[i] != '\0' && !ft_strcmp(tab1[i], tab2[i]))
		i++;
	if (i != ft_tablen(tab1))
		return (i);
	return (0);
}
