#include "libft.h"

size_t		ft_intlen(int n)
{
	size_t	len;

	len = 0;
	if (n < 0)
	{
		len++;
		n = -n;
	}
	if (n == 0)
		return (len + 1);
	while (n)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
