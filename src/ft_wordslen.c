#include "libft.h"

size_t		ft_wordslen(char *str, char c)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i + 1] == '\0')
			return (i + 1);
		if (str[i] == c)
			return (i);
		i++;
	}
	return (0);
}
